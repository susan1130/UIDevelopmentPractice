// string literals
let firstName = "John"
let lastName = "Doe"
console.log(firstName +" "+lastName)            //  John Doe

//Object literals
let user = {
    firstName: "Jane",
    lastName : "Hog"
}
console.log(user)                           //{ firstName: 'Jane', lastName: 'Hog' }

//string literals
let fName = "Pillow"
let lName = "Mina"

let customer = {
    fName : fName,
    lName : lName
}
console.log(customer)                       //{ fName: 'Pillow', lName: 'Mina' }  

//object literals
let cus = {
    fName,
    lName
}
console.log(cus)                            //{ fName: 'Pillow', lName: 'Mina' }  