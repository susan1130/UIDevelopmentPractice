var salary = 50000

switch(salary){
    case 10000: console.log("10k salary")
    break
    case 20000: console.log("20k salary")
    break
    case 30000: console.log("30k salary")
    break
    case 40000: console.log("40k salary")
    break
    case 50000: console.log("50k salary")
    break
    default: console.log("Random salary")
    break
}

//switch case